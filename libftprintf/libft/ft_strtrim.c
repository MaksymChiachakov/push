#include "libft.h"

static int	ft_isinset(char c, char const *set)
{
	size_t	i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*str;

	start = 0;
	while (ft_isinset(s1[start], set))
		start++;
	end = ft_strlen(s1) - 1;
	while (ft_isinset(s1[end], set))
		end--;
	str = ft_substr(s1, start, end - start + 1);
	return (str);
}
